    def _print_model_contribution(self, pairs: list[Any]) -> None:
        """Print breakdown of which models contributed jokes to the pairs."""
        fact_ids = {fid for p in pairs for fid in (p.chosen.id, p.rejected.id)}
        self._print_model_contribution_for_facts(fact_ids)

    def _print_model_contribution_for_facts(self, fact_ids: set[int]) -> None:
        """Print breakdown of which models contributed to the given facts."""
        if not fact_ids:
            return

        rows = self._query_model_usage(fact_ids)
        if not rows:
            return

        print("Model contribution:")
        total = sum(r[1] for r in rows)
        for model_name, count in rows:
            pct = count * 100 / total
            short_name = model_name.split("/")[-1] if "/" in model_name else model_name
            print(f"  {short_name:40s} {count:5d} ({pct:5.1f}%)")
        print()

    def _query_model_usage(self, fact_ids: set[int]) -> list[Any]:
        """Query model usage counts for given fact IDs."""
        assert self._pg is not None
        self.lg.debug("querying model contribution...", extra={"fact_count": len(fact_ids)})
        sql = text("""
            SELECT model_name, COUNT(*) as cnt
            FROM agent_jokester_model_usage
            WHERE fact_id = ANY(:fact_ids)
            GROUP BY model_name
            ORDER BY cnt DESC
        """)
        with self._pg.connect() as conn:
            rows: list[Any] = conn.execute(sql, {"fact_ids": list(fact_ids)}).fetchall()
            return rows

